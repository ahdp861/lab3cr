/**
 * @file morse_code_classic.cpp
 * @brief Программа для преобразования текста в азбуку Морзе (классический стиль)
 * 
 * Программа принимает строку от пользователя и преобразует её в последовательность
 * символов азбуки Морзе согласно официальной таблице для русского алфавита.
 */

#include <iostream>
#include <string>
#include <unordered_map>
#include <cctype>

using namespace std;

// Таблица соответствия русских букв и кода Морзе
const unordered_map<char, string> morseCode = {
    {'А', ".-"}, {'Б', "-..."}, {'В', ".--"}, {'Г', "--."},
    {'Д', "-.."}, {'Е', "."}, {'Ж', "...-"}, {'З', "--.."},
    {'И', ".."}, {'Й', ".---"}, {'К', "-.-"}, {'Л', ".-.."},
    {'М', "--"}, {'Н', "-."}, {'О', "---"}, {'П', ".--."},
    {'Р', ".-."}, {'С', "..."}, {'Т', "-"}, {'У', "..-"},
    {'Ф', "..-."}, {'Х', "...."}, {'Ц', "-.-."}, {'Ч', "---."},
    {'Ш', "----"}, {'Щ', "--.-"}, {'Ъ', "--.--"}, {'Ы', "-.--"},
    {'Ь', "-..-"}, {'Э', "..-.."}, {'Ю', "..--"}, {'Я', ".-.-"},
    {'1', ".----"}, {'2', "..---"}, {'3', "...--"}, {'4', "....-"},
    {'5', "....."}, {'6', "-...."}, {'7', "--..."}, {'8', "---.."},
    {'9', "----."}, {'0', "-----"}, {' ', " "}
};

/**
 * @brief Преобразует символ в код Морзе
 * @param c Входной символ
 * @return Строка с кодом Морзе или пустая строка, если символ не найден
 */
string charToMorse(char c) {
    c = toupper(c);
    auto it = morseCode.find(c);
    if (it != morseCode.end()) {
        return it->second;
    }
    return ""; // Неизвестные символы игнорируются
}

/**
 * @brief Преобразует строку в код Морзе
 * @param text Входная строка
 * @return Строка с кодом Морзе
 */
string stringToMorse(const string& text) {
    string result;
    for (char c : text) {
        string morse = charToMorse(c);
        if (!morse.empty()) {
            if (!result.empty() && result.back() != ' ') {
                result += " ";
            }
            result += morse;
        }
    }
    return result;
}

int main() {
    cout << "Введите сообщение для преобразования в код Морзе: ";
    string input;
    getline(cin, input);
    
    string morse = stringToMorse(input);
    cout << "Код Морзе: " << morse << endl;
    
    return 0;
}